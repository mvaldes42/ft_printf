#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	result;
	int	j;

	i = 0;
	j = 0;
	result = 0;
	if (str == NULL || str[i] == '\0')
		return (0);
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			j++;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = (str[i] - '0') + result * 10;
		i++;
	}
	if (j % 2 == 0)
		return (result);
	return (-1 * result);
}
